    public void PreencherTabela() {

        FornecedorDAO dao = new FornecedorDAO();
        String nome = txtPesquisar.getText();

        List<Fornecedor> lista = dao.listaNomeID(nome);
        DefaultTableModel modelo = (DefaultTableModel) tabelaFornecedorID.getModel();
        tabelaFornecedorID.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);

        for (Fornecedor f : lista) {
            Object[] obj = new Object[]{
                f.getId(),
                f.getNome()
            };
            modelo.addRow(obj);
        }

    }

