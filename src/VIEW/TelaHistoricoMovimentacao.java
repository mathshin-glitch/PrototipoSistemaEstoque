    private void aplicarRendererPersonalizado() {

        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {

            @Override
            public Component getTableCellRendererComponent(
                    JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {

                Component c = super.getTableCellRendererComponent(
                        table, value, isSelected, hasFocus, row, column
                );

                // LINHAS DE ZEBRA
                if (!isSelected) {
                    if (row % 2 == 0) {
                        c.setBackground(Color.WHITE);
                    } else {
                        c.setBackground(new Color(245, 245, 245));
                    }
                }

                // Dados para cores
                String tipo = table.getValueAt(row, 2).toString(); // Coluna TIPO
                int quantidade = Integer.parseInt(table.getValueAt(row, 3).toString());

                // COLUNA DATA
                if (column == 0) {
                    c.setBackground(new Color(230, 240, 255));
                }

                // COLUNA TIPO (entrada/sa√≠da)
                if (column == 2) {
                    if (tipo.equalsIgnoreCase("ENTRADA")) {
                        c.setBackground(new Color(210, 255, 210));
                        c.setForeground(new Color(0, 100, 0));
                    } else {
                        c.setBackground(new Color(255, 220, 220));
                        c.setForeground(new Color(150, 0, 0));
                    }
                    setHorizontalAlignment(CENTER);
                }

                // COLUNA QUANTIDADE
                if (column == 3) {
                    if (tipo.equalsIgnoreCase("ENTRADA")) {
                        c.setForeground(new Color(0, 130, 0));
                        setText("+" + quantidade);
                    } else {
                        c.setForeground(new Color(180, 0, 0));
                        setText("-" + quantidade);
                    }
                    setHorizontalAlignment(CENTER);
                }

                // COLUNA FORNECEDOR
                if (column == 4) {
                    c.setBackground(new Color(240, 240, 255));
                }

                return c;
            }
        });
    }

    public void carregarTabela() {
        MovimentacaoEstoqueDAO dao = new MovimentacaoEstoqueDAO();
        List<Object[]> historico = dao.listarMovimentacoes();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); // Limpa a Tabela

        for (Object[] linha : historico) {
            modelo.addRow(linha);
        }

    }

